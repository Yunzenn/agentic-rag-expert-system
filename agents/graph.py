"""
Agentic RAG Knowledge Base Expert System - LangGraph State Machine

This module defines the core state machine for the RAG system using LangGraph.
The graph orchestrates the retrieval, relevance evaluation, web search fallback,
and answer generation processes through a series of stateful nodes and edges.

Design Philosophy:
- The State serves as the single source of truth across all nodes
- Each node is a pure function that transforms the State
- Edges represent conditional logic for routing between nodes
- The graph supports self-correction loops through feedback mechanisms
"""

