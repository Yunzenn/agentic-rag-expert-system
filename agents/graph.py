def build_graph() -> StateGraph:
    """
    Constructs the complete LangGraph state machine with CRAG decision logic.
    
    Mermaid Flowchart:
    ```mermaid
    graph TD
        Start([开始]) --> Retrieval[检索节点]
        Retrieval --> Evaluation[相关性评估]
        Evaluation -->|置信度 >= 7| Generation[生成节点]
        Evaluation -->|3 <= 置信度 < 7| Rewrite[查询重写]
        Evaluation -->|置信度 < 3| WebSearch[网络搜索]
        Rewrite --> Evaluation
        WebSearch --> Generation
        Generation --> End([结束])
    ```
    
    CRAG Graph Flow:
    1. retrieval_node -> relevance_evaluation_node
    2. relevance_evaluation_node -> [generation | query_rewrite | web_search] (conditional by confidence)
    3. query_rewrite_node -> relevance_evaluation_node (loop for re-evaluation)
    4. web_search_node -> generation_node
    5. generation_node -> END
    
    Returns:
        Compiled StateGraph ready for execution
    """
    workflow = StateGraph(RAGState)
    
    # Add nodes to the graph
    workflow.add_node("retrieval", retrieval_node)
    workflow.add_node("relevance_evaluation", relevance_evaluation_node)
    workflow.add_node("query_rewrite", query_rewrite_node)
    workflow.add_node("web_search", web_search_node)
    workflow.add_node("generation", generation_node)
    
    # Define edges (sequential flow)
    workflow.set_entry_point("retrieval")
    workflow.add_edge("retrieval", "relevance_evaluation")
    
    # Conditional edge: CRAG routing based on confidence
    workflow.add_conditional_edges(
        "relevance_evaluation",
        route_by_confidence,
        {
            "generate": "generation",
            "query_rewrite": "query_rewrite",
            "web_search": "web_search"
        }
    )
    
    # Query rewrite loops back to evaluation
    workflow.add_edge("query_rewrite", "relevance_evaluation")
    
    # Web search proceeds to generation
    workflow.add_edge("web_search", "generation")
    
    # Generation ends the workflow
    workflow.add_edge("generation", END)
    
    return workflow.compile()


